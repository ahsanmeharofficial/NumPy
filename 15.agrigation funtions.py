import numpy as np 
arr = np.array([10,20,30,40,50])

print (np.sum(arr))  
print (np.mean(arr))#Array ka average 
print (np.min(arr))
print (np.max(arr))
print (np.std(arr))#Standard Deviation tells you how much the data values vary or spread around the mean (average).
print (np.var(arr))#Spread ka square (Standard Deviation²)